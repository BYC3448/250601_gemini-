  // 선수학습 내용 가져오기 함수
  export function getPrerequisiteContent(schoolLevel, gradeGroup) {
      const prerequisites = [];
      
      // 선수학습 경로 가져오기
      const paths = prerequisiteData[schoolLevel]?.[gradeGroup] || [];
      
      // 각 경로에 대한 내용 요소 수집
      paths.forEach(path => {
          const [preSchoolLevel, preGradeGroup] = path.split('/');
          
          const subjects = curriculumData[preSchoolLevel]?.[preGradeGroup] || {};
          
          // 해당 학교급과 학년군의 모든 과목에 대한 내용 요소 수집
          for (const subject in subjects) {
              const units = subjects[subject];
              
              // 각 단원의 내용 요소 모으기
              for (const unit in units) {
                  const contentElements = units[unit];
                  
                  prerequisites.push({
                      schoolLevel: preSchoolLevel,
                      gradeGroup: preGradeGroup,
                      subject,
                      unit,
                      contentElements
                  });
              }
          }
      });
      
      return prerequisites;
  }
  
